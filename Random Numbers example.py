import random

# Now that we have the random method in our program, let's see
# what different things we can do with it.

# This will get a random integer in the range 0-100 and print
# that random int 5 times.
x = random.randint(0,100)
print(x)
x = random.randint(0,100)
print(x)
x = random.randint(0,100)
print(x)
x = random.randint(0,100)
print(x)
